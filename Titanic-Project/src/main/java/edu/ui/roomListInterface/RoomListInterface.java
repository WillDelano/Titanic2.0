package edu.ui.roomListInterface;

/**
 * Interface for displaying a list of rooms.
 *
 * This interface defines a contract for classes that need to display a list of rooms.
 * Implementing classes should provide functionality to show information about rooms.
 *
 * @see edu.ui.guestBrowseRooms.BrowseRoomPage
 */
public interface RoomListInterface {

    /**
     * Displays the given page instance.
     */
    void show();
}
